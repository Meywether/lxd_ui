namespace Controller\Api\Lxd\Containers\Logs;

/**
 *
 */
class Index extends \Base\Controller
{
    /*
     * @var
     */
    private $lxd;

    /*
     * @var
     */
    protected $body = [];
    
    /*
     * @var
     */
    protected $result = []; 
    
    /*
     * @var
     */
    protected $errors = []; 

    /**
     * @param object $f3
     * @return void
     */
    public function beforeRoute(\Base $f3)
    {
        parent::beforeRoute($f3);
        
        try {
            \Lib\JWT::checkAuth();
        } catch (\Exception $e) {
            $f3->response->json([
                'error' => $e->getMessage(),
                'code'  => $e->getCode(),
                'data'  => []
            ]);
        }

        // check feature is enabled
        if (!in_array('containers', $f3->get('modules.lxd'))) {
            $f3->status(404);
            $f3->response->json([
                'error' => 'Feature not enabled',
                'code'  => 404,
                'data'  => []
            ]);
        }

        // define model/s
        $this->lxd = new \Model\LXD($f3);
    }

    /**
     * GET /api/lxd/containers/@name/logs
     *
     * @param object $f3
     * @return void
     */
    public function get(\Base $f3)
    {
        try {
            $result = $this->lxd->containers->logs->list('local', $f3->get('PARAMS.name'));
                
            if (!empty($result)) {
                foreach ($result as &$log) {
                    $log = str_replace('/1.0/containers/'.$f3->get('PARAMS.name').'/logs/', '', $log);  
                    $log = [
                        'name' => $log,
                        'data' => $this->lxd->containers->logs->get('local', $f3->get('PARAMS.name'), $log)
                    ];
                }
            }

            $this->result = [
                'error' => '',
                'code'  => 200,
                'data'  => $result
            ];
        } catch (\Exception $e) {
            $this->result = [
                'error' => $e->getMessage(),
                'code'  => 422,
                'data'  => []
            ];
        }
    }

}
